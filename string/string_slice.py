x = "python"

# first charecter
print(x[0])

# last charecter
print(x[-1])

# first 3 charecter
print(x[0:3])

# last 3 charecter
print(x[-3:6])

# print from 3rd position
print(x[2:])

# print will stop at 3rd position
print(x[:3])

# step argument
print(x[0:5:2])

# string reverse
print(x[::-1])